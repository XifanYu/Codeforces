const int Maxn=1e5+5;

int n;
char s[Maxn];
int cnt[256];

int main()
{
	scanf("%d",&n);
	scanf("%s",s);
	if(n==1)
	{
		cout<<"Yes\n";
		return 0;
	}
	clean(cnt,0);
	for(int i=0;i<n;i++)
	{
		cnt[s[i]]++;
	}
	for(int i='a';i<='z';i++)
	{
		if(cnt[i]>=2)
		{
			cout<<"Yes\n";
			return 0;
		}
	}
	cout<<"No\n";
	return 0;
}
